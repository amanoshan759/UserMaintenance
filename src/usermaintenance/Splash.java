    public Splash() {
        initComponents();

        ProgressBar.setMaximum(maxValue);
        timer = new Timer(interval, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if (i == (maxValue - 1)) {
                    timer.stop();
                    LoginPage obj1 = new LoginPage();
                    obj1.setVisible(true);
                    Splash.this.dispose();
                }
                i++;
                ProgressBar.setValue(i);
            }
        });
        timer.start();
    }
    
